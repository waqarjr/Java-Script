// Nodejs is a runtime environment for executing javascript code outside the browser.
// It is a javascript runtime built on chrome's v8 engine.
// It is a single threaded, event driven, non-blocking I/O model.
// It is a open source and cross-platform.

// Key Differences between Node.js and Browser JavaScript:

// 1. Runtime Environment:
//    - Browser JS: Runs in web browsers (Chrome, Firefox, Safari, etc.)
//    - Node.js: Runs on servers and computers outside the browser

// 2. Global Objects:
//    - Browser JS: Has 'window' as global object, 'document' for DOM manipulation
//    - Node.js: Has 'global' as global object, no DOM access

// 3. Modules:
//    - Browser JS: Uses ES6 modules with import/export
//    - Node.js: Uses CommonJS modules with require() and module.exports

// 4. File System Access:
//    - Browser JS: Limited file system access (only through user interaction)
//    - Node.js: Full file system access through 'fs' module

// 5. Network Operations:
//    - Browser JS: Limited to HTTP/HTTPS requests (XMLHttpRequest, Fetch API)
//    - Node.js: Can create servers, handle TCP/UDP connections, etc.

// 6. Use Cases:
//    - Browser JS: Frontend development, UI interactions, DOM manipulation
//    - Node.js: Backend development, server-side applications, APIs, file operations

// 7. Package Management:
//    - Browser JS: Uses CDNs or bundlers (Webpack, Parcel)
//    - Node.js: Uses npm (Node Package Manager)

// 8. Event Loop:
//    - Browser JS: Single-threaded with Web APIs for async operations
//    - Node.js: Single-threaded with libuv for async I/O operations

// 9. Security:
//    - Browser JS: Sandboxed environment with same-origin policy
//    - Node.js: Direct system access, requires careful security considerations

// 10. Performance:
//     - Browser JS: Optimized for UI rendering and user interactions
//     - Node.js: Optimized for I/O operations and server-side processing

