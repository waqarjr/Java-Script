// What is “State” in React?
// State in React is a built-in object that stores dynamic data of a component.
// When the state changes, React automatically re-renders the component to reflect the updated UI.
// In simple words:
// State = data that can change over time and controls what is shown on the screen.

// Why do we need state?
// Because normal variables do NOT trigger re-render.

// What is State Management in React?
// State management is the process of creating, updating, sharing, and controlling state across 
// components in a React application in a predictable way.
// In simple words:
// How we handle and share data across our app.

